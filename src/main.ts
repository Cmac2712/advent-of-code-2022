const PlayMap = new Map();
const Outcome = new Map<string, string>();
const ElfPlayMap = new Map();
const ScoreMap = new Map();

ElfPlayMap.set("A", 1); // Rock
ElfPlayMap.set("B", 2); // Paper
ElfPlayMap.set("C", 3); // Scissors

PlayMap.set("X", 1); // Rock
PlayMap.set("Y", 2); // Paper
PlayMap.set("Z", 3); // Scissors

Outcome.set("X", "LOSS");
Outcome.set("Y", "DRAW");
Outcome.set("Z", "WIN");

ScoreMap.set("WIN", 6);
ScoreMap.set("DRAW", 3);
ScoreMap.set("LOSS", 0);

type GetScore = (player1: string, player2: string) => number;

const getScore: GetScore = (player1, player2) => {
  const player1Play = ElfPlayMap.get(player1);
  const player2Play = PlayMap.get(player2);

  // DRAW
  if (player2Play === player1Play) {
    return ScoreMap.get("DRAW");
  }

  // Rock beats Scissors
  if (player2Play === 1 && player1Play === 3) {
    return ScoreMap.get("WIN");
  }

  // Paper beats Rock
  if (player2Play === 2 && player1Play === 1) {
    return ScoreMap.get("WIN");
  }

  // Scissors beats Paper
  if (player2Play === 3 && player1Play === 2) {
    return ScoreMap.get("WIN");
  }

  return ScoreMap.get("LOSS");
};

